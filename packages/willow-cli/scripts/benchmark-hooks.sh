#!/bin/bash

# Pre-commit hooks performance benchmarking script
# Tests execution time with different commit sizes

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(dirname "$SCRIPT_DIR")"
BENCHMARK_DIR="$PROJECT_ROOT/.benchmark"

# Colors for output
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

echo -e "${BLUE}🚀 Benchmarking pre-commit hooks performance...${NC}"

# Create benchmark directory
mkdir -p "$BENCHMARK_DIR"
cd "$PROJECT_ROOT"

# Function to measure time in milliseconds
time_ms() {
  if [[ "$OSTYPE" == "darwin"* ]]; then
    # macOS
    python3 -c "import time; print(int(time.time() * 1000))"
  else
    # Linux
    date +%s%3N
  fi
}

# Function to create test files of different sizes
create_test_files() {
  local file_count=$1
  local file_size=$2
  
  echo "Creating $file_count files (~$file_size lines each)..."
  
  for i in $(seq 1 $file_count); do
    {
      echo "// Test file $i - autogenerated for benchmarking"
      echo "export interface TestInterface$i {"
      
      for j in $(seq 1 $file_size); do
        echo "  property$j: string;"
      done
      
      echo "}"
      echo ""
      echo "export class TestClass$i implements TestInterface$i {"
      
      for j in $(seq 1 $file_size); do
        echo "  property$j = 'value$j';"
      done
      
      echo "}"
    } > "$BENCHMARK_DIR/test-file-$i.ts"
  done
}

# Function to run benchmark
run_benchmark() {
  local scenario=$1
  local file_count=$2
  local file_size=$3
  
  echo -e "${YELLOW}📊 Scenario: $scenario${NC}"
  echo "Files: $file_count, Lines per file: ~$file_size"
  
  # Clean up previous benchmark files
  rm -rf "$BENCHMARK_DIR"/*.ts
  
  # Create test files
  create_test_files $file_count $file_size
  
  # Stage files
  git add "$BENCHMARK_DIR"/*.ts 2>/dev/null || true
  
  # Measure lint-staged execution time
  echo "Measuring lint-staged performance..."
  start_time=$(time_ms)
  
  if npx lint-staged --verbose > "$BENCHMARK_DIR/lint-staged-output.txt" 2>&1; then
    end_time=$(time_ms)
    duration=$((end_time - start_time))
    echo "✅ Lint-staged completed in ${duration}ms"
  else
    echo "❌ Lint-staged failed"
    duration=-1
  fi
  
  # Reset staged files
  git reset HEAD "$BENCHMARK_DIR"/*.ts 2>/dev/null || true
  
  # Clean up
  rm -f "$BENCHMARK_DIR"/*.ts
  
  echo "$scenario,$file_count,$file_size,$duration" >> "$BENCHMARK_DIR/benchmark-results.csv"
  echo ""
}

# Initialize results file
echo "Scenario,FileCount,LinesPerFile,DurationMs" > "$BENCHMARK_DIR/benchmark-results.csv"

echo "Starting performance benchmarks..."
echo ""

# Test scenarios
run_benchmark "Small commit" 3 20
run_benchmark "Medium commit" 8 50  
run_benchmark "Large commit" 15 100
run_benchmark "Very large commit" 25 150

# Generate performance report
echo -e "${BLUE}📋 Generating performance report...${NC}"

cat > "$BENCHMARK_DIR/performance-report.md" << EOF
# Pre-commit Hooks Performance Report

Generated: $(date)

## Benchmark Results

| Scenario | Files | Lines/File | Duration (ms) | Performance |
|----------|-------|------------|---------------|-------------|
EOF

# Read results and add to report
while IFS=',' read -r scenario files lines duration; do
  if [[ "$scenario" != "Scenario" ]]; then
    if [[ $duration -gt 0 ]]; then
      if [[ $duration -lt 5000 ]]; then
        performance="🟢 Excellent"
      elif [[ $duration -lt 10000 ]]; then
        performance="🟡 Good"
      else
        performance="🔴 Needs optimization"
      fi
    else
      performance="❌ Failed"
    fi
    
    echo "| $scenario | $files | $lines | $duration | $performance |" >> "$BENCHMARK_DIR/performance-report.md"
  fi
done < "$BENCHMARK_DIR/benchmark-results.csv"

cat >> "$BENCHMARK_DIR/performance-report.md" << EOF

## Performance Targets
- **Target**: < 10 seconds (10,000ms) for typical commits
- **Excellent**: < 5 seconds (5,000ms)
- **Good**: 5-10 seconds
- **Needs optimization**: > 10 seconds

## Tools Measured
- ESLint with --fix
- Prettier formatting
- File staging/unstaging

## Recommendations
- Commits with 3-8 files perform optimally
- Very large commits (>25 files) may benefit from:
  - Breaking into smaller commits
  - Using \`--no-verify\` flag for emergency commits
  - Running \`npm run quality:full\` separately

EOF

echo -e "${GREEN}✅ Benchmarking complete!${NC}"
echo -e "${BLUE}📋 Results saved to: $BENCHMARK_DIR/performance-report.md${NC}"
echo ""

# Display summary
echo "Performance Summary:"
tail -n +2 "$BENCHMARK_DIR/benchmark-results.csv" | while IFS=',' read -r scenario files lines duration; do
  echo "  $scenario: ${duration}ms"
done